<template>
    <div>
        <h3>用户列表页面</h3>
        <a-card>
            <a-row :gutter="20">
                <a-col :span="6">
                    <a-input-search v-model:value="queryParam.username" placeholder="输入用户名查找" enter-button
                        @search="getUserList" />
                </a-col>
                <a-col :span="4">
                    <a-button type="primary">新增用户</a-button>
                </a-col>
            </a-row>
            <a-table rowKey="username" :columns="columns" :pagination="pagination" :dataSource="userlist" bordered
                allowClear @change="handleTableChange">
                <template v-slot:bodyCell="{ column, record }">
                    <template v-if="column.dataIndex === 'action'">
                        <div class="actionSlot">
                            <a-button type="primary" style="margin-right: 16px;">编辑</a-button>
                            <a-button type="primary" danger ghost @click="deleteUser(record.ID)">删除</a-button>
                        </div>
                    </template>
                    <template v-if="column.dataIndex === 'role'">
                        <span>{{ record.role === 1 ? '管理员' : "普通用户" }}</span>
                    </template>
                </template>
            </a-table>
        </a-card>
    </div>
</template>


<script>

const columns = [
    {
        title: 'ID',
        dataIndex: 'ID',
        width: '10%',
        key: 'id',
        align: 'center',
    },
    {
        title: '用户名',
        dataIndex: 'username',
        width: '20%',
        key: 'username',
        align: 'center',
    },
    {
        title: '角色',
        dataIndex: 'role',
        width: '20%',
        key: 'role',
        align: 'center',
    },
    {
        title: '操作',
        width: '30%',
        key: 'action',
        dataIndex: 'action',
    },
]

export default {
    data() {
        return {
            pagination: {
                pageSizeOptions: ['5', '10', '20'],
                pageSize: 5,
                total: 0,
                showSizeChanger: true,
                showTotal: (total) => `共${total}条`,
            },
            userlist: [],
            columns,
            queryParam: {
                username: '',
                pagenum: 1,
                pagesize: 5,
            },
        }
    },
    created() {
        this.getUserList()
    },
    methods: {
        async getUserList() {
            const { data: res } = await this.$http.get('users', {
                params: {
                    username: this.queryParam.username,
                    pagesize: this.queryParam.pagesize,
                    pagenum: this.queryParam.pagenum,
                },
            })
            if (res.status !== 200) return this.$message.error(res.message)
            this.userlist = res.data
            this.pagination.total = res.total
        },

        // 更改页码
        handleTableChange(pagination) {
            var pager = { ...this.pagination }
            pager.current = pagination.current
            pager.pageSize = pagination.pageSize
            this.queryParam.pagesize = pagination.pageSize
            this.queryParam.pagenum = pagination.current


            if (pagination.pageSize !== this.pagination.pageSize) {
                this.queryParam.pagenum = 1
                pager.current = 1
            }
            this.pagination = pager
            this.getUserList()
        },

        // 删除用户
        deleteUser(id) {
            this.$confirm({
                title: '确定要删除该用户吗?',
                content: '一旦删除，无法恢复',
                okText: 'Yes',
                okType: 'danger',
                cancelText: 'No',
                onOk: async () => {
                    const res = await this.$http.delete(`user/${id}`)
                    if (res.status !== 200) return this.$message.error(res.message)
                    this.$message.success('删除成功')
                    this.getUserList()
                },
                onCancel: () => {
                    this.$message.info('已取消删除')
                },
            });

        },
    },
}
</script>


<style scoped>
.actionSlot {
    display: flex;
    justify-content: center;
}
</style>